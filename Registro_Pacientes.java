    private void jbHEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbHEliminarActionPerformed
         if (historialSeleccionado != null) {
            if (controladorHistorialClinicodb.eliminar(historialSeleccionado)) {
                JOptionPane.showMessageDialog(rootPane, "Historial Clinico eliminado con éxito del sitema.");
                limpiarHistorial();
                historialSeleccionado = null;
               
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede eliminar el historial seleccionado.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay historial seleccionado para eliminar.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbHEliminarActionPerformed

