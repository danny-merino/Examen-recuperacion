    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       if(controladorPacientedb.getPacientesCedula(txtCedula.getText())!=null){
       JOptionPane.showMessageDialog(rootPane,"La persona con ese numeronde cedula ya se encuntra registrada","ERROR",JOptionPane.ERROR_MESSAGE);
       }else{
       Pacientes pacienteGuardar = guardarEditar();
        if(pacienteGuardar!=null){
            if(controladorPacientedb.registarPaciente(pacienteGuardar)){
        JOptionPane.showMessageDialog(rootPane,"Paciente guardado exitosamente");
        limpiarCamposPacientes();
        }else{
        JOptionPane.showMessageDialog(rootPane,"No se puede guardar el paciente","ERROR",JOptionPane.ERROR_MESSAGE);
        }
       
       }
        
        
            
            
        }
        
       
    }//GEN-LAST:event_btnguardarActionPerformed

    public Pacientes guardarEditar(){
    
    if(txtCedula.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo cedula no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtCedula.requestFocus();
           return null;
        }
        if(!utilidades.validadorDeCedula(txtCedula.getText())){
        JOptionPane.showMessageDialog(rootPane,"Lacedula ingresada no es valida","ERROR",JOptionPane.ERROR_MESSAGE);
        }
         if(txtNombre.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo nombre no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtNombre.requestFocus();
           return null;
        }
          if(txtApellido.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo apellido no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtApellido.requestFocus();
           return null;
        }
           if(txtDireccion.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo direccion no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtDireccion.requestFocus();
           return null;
        }
            if(txtCorreo.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo correo no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtCorreo.requestFocus();
           return null;
        }
            if(!utilidades.validarCorreo(txtCorreo.getText())){
            JOptionPane.showMessageDialog(rootPane,"Los datos ingresados en correo no son validos","ERROR",JOptionPane.ERROR_MESSAGE);
            }
             if(txtTelefono.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"El campo telefono no tiene datos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtTelefono.requestFocus();
           return null;
        }
             if(!utilidades.validarNumeros(txtTelefono.getText())){
             JOptionPane.showMessageDialog(rootPane,"Los datos ingresados en telefono no son validos","ERROR",JOptionPane.ERROR_MESSAGE);
             }
        Pacientes pacientes = new Pacientes ();
        pacientes.setCedula(txtCedula.getText());
        pacientes.setNombre(txtNombre.getText());
        pacientes.setApellido(txtApellido.getText());
        pacientes.setDireccion(txtDireccion.getText());
        pacientes.setCorreo(txtCorreo.getText());
        pacientes.setTelefono(txtTelefono.getText());
        return pacientes;
        
        
    
    }
    
    
    
    
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
       if(pacienteEditarEliminar ==null){
           JOptionPane.showMessageDialog(rootPane,"No hay una persona seleccionada para editar","ERROR",JOptionPane.ERROR_MESSAGE);
       return;
       
       }
       
       Pacientes pacienteEditarlocal = guardarEditar();
       if(pacienteEditarlocal!=null){
           pacienteEditarlocal.setIdpacientes(pacienteEditarEliminar.getIdpacientes());
        controladorPacientedb.actualizar(pacienteEditarlocal);
         JOptionPane.showMessageDialog(rootPane,"Paciente editado con exito ");
        limpiarCamposPacientes();
        pacienteEditarEliminar = null;
       }else{
        JOptionPane.showMessageDialog(rootPane,"No se puedo editar el paciente","ERROR",JOptionPane.ERROR_MESSAGE);
        }
       
        

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnTraerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraerActionPerformed
        // TODO add your handling code here:
        List<Pacientes> obtenerPacientes = controladorPacientedb.obtenerPacientes();
        pacienteEditarEliminar = obtenerPacientes.get(obtenerPacientes.size()-1);
        txtCedula.setText(pacienteEditarEliminar.getCedula());
        txtNombre.setText(pacienteEditarEliminar.getNombre());
        txtApellido.setText(pacienteEditarEliminar.getApellido());
        txtDireccion.setText(pacienteEditarEliminar.getCorreo());
        txtTelefono.setText(pacienteEditarEliminar.getTelefono());
    }//GEN-LAST:event_btnTraerActionPerformed

    private void btnBuscarCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCedulaActionPerformed
        Pacientes paciente = controladorPacientedb.getPacientesCedula(txtCedula.getText());
        if(paciente!=null){
        txtNombre.setText(paciente.getNombre());
        txtApellido.setText(paciente.getApellido());
        txtDireccion.setText(paciente.getDireccion());
        txtCorreo.setText(paciente.getCorreo());
        txtTelefono.setText(paciente.getTelefono());
        pacienteEditarEliminar = paciente;
         }else{
        JOptionPane.showMessageDialog(rootPane,"Paciente no encontrado en el sistema","ERROR",JOptionPane.ERROR_MESSAGE);
        txtCedula.setText("");
        txtCedula.requestFocus();
        }
       
        
        
        
    }//GEN-LAST:event_btnBuscarCedulaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      if(pacienteEditarEliminar!=null){
          if(controladorPacientedb.eliminar(pacienteEditarEliminar)){
             JOptionPane.showMessageDialog(rootPane,"Paciente eliminado con exito del sistema "); 
             limpiarCamposPacientes();
          }else{
           JOptionPane.showMessageDialog(rootPane,"No se puede eliminar la persona selecionada","ERROR",JOptionPane.ERROR_MESSAGE);
          }
       
      }else{
      JOptionPane.showMessageDialog(rootPane,"No hay paciente seleccionado para eliminar","ERROR",JOptionPane.ERROR_MESSAGE);
      }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

