    private void jbHEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbHEditarActionPerformed
         if (historialSeleccionado == null) {
            JOptionPane.showMessageDialog(rootPane, "No hay un historial seleccionado para editar", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (historialSeleccionado != null) {
            
            if (controladorHistorialClinicodb.actualizar(historialSeleccionado)) {
                JOptionPane.showMessageDialog(rootPane, "Producto editado con exito del sitema.");
                limpiarHistorial();
                historialSeleccionado = null;
               
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede editar la Producto", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbHEditarActionPerformed

